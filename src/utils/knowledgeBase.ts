export const KNOWLEDGE_BASE: KnowledgeBase = {
  planets: KNOWLEDGE_PLANETS,
  signs: KNOWLEDGE_SIGNS,
  houses: KNOWLEDGE_HOUSES,
  elements: ELEMENTS,
  modalities: MODALITIES,
  planetSignCombos: PLANET_SIGN_COMBOS,
  planetHouseCombos: PLANET_HOUSE_COMBOS,
  signHouseCombos: SIGN_HOUSE_COMBOS,
};

export function getKnowledgePlanet(name: string): KnowledgePlanet | undefined {
  return KNOWLEDGE_PLANETS.find(p => p.name === name);
}

export function getKnowledgeSign(name: string): KnowledgeSign | undefined {
  return KNOWLEDGE_SIGNS.find(s => s.name === name);
}

export function getKnowledgeHouse(number: number): KnowledgeHouse | undefined {
  return KNOWLEDGE_HOUSES.find(h => h.number === number);
}

export function getElement(name: string): Element | undefined {
  return ELEMENTS.find(e => e.name === name);
}

export function getModality(name: string): Modality | undefined {
  return MODALITIES.find(m => m.name === name);
}

export function getPlanetSignCombo(planet: string, sign: string): PlanetSignCombo | undefined {
  return PLANET_SIGN_COMBOS.find(c => c.planet === planet && c.sign === sign);
}

export function getPlanetHouseCombo(planet: string, house: number): PlanetHouseCombo | undefined {
  return PLANET_HOUSE_COMBOS.find(c => c.planet === planet && c.house === house);
}

export function getSignHouseCombo(sign: string, house: number): SignHouseCombo | undefined {
  return SIGN_HOUSE_COMBOS.find(c => c.sign === sign && c.house === house);
}

export function searchKnowledge(query: string): {
  planets: KnowledgePlanet[];
  signs: KnowledgeSign[];
  houses: KnowledgeHouse[];
  elements: Element[];
  modalities: Modality[];
} {
  const q = query.toLowerCase();
  return {
    planets: KNOWLEDGE_PLANETS.filter(p =>
      p.name.toLowerCase().includes(q) ||
      p.keywords.some(k => k.toLowerCase().includes(q)) ||
      p.meaning.toLowerCase().includes(q)
    ),
    signs: KNOWLEDGE_SIGNS.filter(s =>
      s.name.toLowerCase().includes(q) ||
      s.keywords.some(k => k.toLowerCase().includes(q)) ||
      s.meaning.toLowerCase().includes(q)
    ),
    houses: KNOWLEDGE_HOUSES.filter(h =>
      h.name.toLowerCase().includes(q) ||
      h.keywords.some(k => k.toLowerCase().includes(q)) ||
      h.meaning.toLowerCase().includes(q)
    ),
    elements: ELEMENTS.filter(e =>
      e.name.toLowerCase().includes(q) ||
      e.traits.some(t => t.toLowerCase().includes(q)) ||
      e.meaning.toLowerCase().includes(q)
    ),
    modalities: MODALITIES.filter(m =>
      m.name.toLowerCase().includes(q) ||
      m.traits.some(t => t.toLowerCase().includes(q)) ||
      m.meaning.toLowerCase().includes(q)
    ),
  };
}
